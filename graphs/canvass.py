tiles_groups = [f"Group {i}" for i in range(9, 16)]

# =========================
# COMBINE DATA PROPERLY
# =========================

def combine_groups(group_list):
    combined = {coin: [] for coin in coin_order}
    all_flips = []
    
    for group_name in group_list:
        group = groups_data.get(group_name, {})
        for coin_class, flips in group.items():
            first_100 = flips[:100]
            if coin_class in combined:
                combined[coin_class].extend(first_100)
            all_flips.extend(first_100)
    
    return combined, all_flips


wood_data, wood_all = combine_groups(wood_groups)
tiles_data, tiles_all = combine_groups(tiles_groups)

# =========================
# CUMULATIVE FUNCTION
# =========================

def cumulative(H_list):
    cumH, cumT = [], []
    h_sum, t_sum = 0, 0
    for h in H_list:
        h_sum += h
        t_sum += (1 - h)
        cumH.append(h_sum)
        cumT.append(t_sum)
    return cumH, cumT

# =========================
# ANIMATION FUNCTION
# =========================

def animate_graph(H_list, title):
    if len(H_list) == 0:
        return
        
    cumH, cumT = cumulative(H_list)
    trials = list(range(1, len(H_list)+1))
    
    fig, ax = plt.subplots(figsize=(10,6))
    ax.set_xlim(1, len(H_list))
    ax.set_ylim(0, len(H_list))
    
    step = max(1, len(H_list)//10)
    ax.set_xticks(range(0, len(H_list)+1, step))
    ax.set_yticks(range(0, len(H_list)+1, step))
    
    ax.set_title(title, fontsize=14, fontweight='bold')
    ax.set_xlabel("Number of Trials")
    ax.set_ylabel("Cumulative Count")
    
    lineH, = ax.plot([], [], linewidth=2)
    lineT, = ax.plot([], [], linewidth=2, linestyle="--")
    
    ax.legend(["Heads", "Tails"])
    
    def update(frame):
        lineH.set_data(trials[:frame], cumH[:frame])
        lineT.set_data(trials[:frame], cumT[:frame])
        return lineH, lineT
    
    ani = FuncAnimation(fig, update, frames=len(H_list)+1, interval=5, repeat=False)
    plt.tight_layout()
    plt.show()

# =========================
# RUN PER COIN CLASS
# =========================

for coin in coin_order:
    
    # WOOD
    if len(wood_data[coin]) > 0:
        animate_graph(
            wood_data[coin],
            f"WOOD (Groups 1–8) - {coin} Coin"
        )
    
    # TILES
    if len(tiles_data[coin]) > 0:
        animate_graph(
            tiles_data[coin],
            f"TILES (Groups 9–15) - {coin} Coin"
        )

